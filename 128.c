#include<stdio.h>
#include <stdio.h>

int main() {
	int n, c, x, i;
	c = 0;
	scanf("%d", &n);
	for(i = 0; i < n ; i++ ) {
		c = c + x;
	}
	
	printf("%.2f\n", 1.0*c/ n);
}
